    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (s_id.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "من فضلك قم بأختيار طالب من الجدول");
        } else {
            try {

                Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
                con = DriverManager.getConnection("jdbc:ucanaccess://./mydb.accdb");
                String sql = "DELETE FROM student WHERE ID_Student=?";

                PreparedStatement sta = con.prepareStatement(sql);
                sta.setString(1, s_id.getText());

                int rowsDeleted = sta.executeUpdate();
                if (rowsDeleted > 0) {
                    JOptionPane.showMessageDialog(null, "تم حذف الموظف بنجاح");
                }

                tableupdate();

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(MainPage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        checkprint();

    }//GEN-LAST:event_jButton2ActionPerformed

