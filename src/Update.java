    public void get_id() {
        id = 0;
        ResultSet rs;

        try {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            con = DriverManager.getConnection("jdbc:ucanaccess://./mydb.accdb");

            String sql = "select * from date where Lectuer_Name='" + name.getText() + "' and Cource_Name ='" + course.getText() + "' and Date_Time ='" + date.getText() + "'";

            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();

            while (rs.next()) {
                id = rs.getInt("ID_Cource");
            }
            if (id == 0) {
                JOptionPane.showMessageDialog(null, "لم يتم اايجاد الكورس برجاء التأكد من البيانات");
            }
            pst.execute();
            pst.close();

        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void tableupdate() {
        ResultSet rs;

        try {

            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            con = DriverManager.getConnection("jdbc:ucanaccess://./mydb.accdb");
            pst = con.prepareStatement("select * from student where Course_Num =  '" + id + "'");
            rs = pst.executeQuery();

            DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
            dft.setRowCount(0);

            while (rs.next()) {
                Vector v3 = new Vector();
                for (int i = 0; i < 2; i++) {
                    v3.add(rs.getString("ID_Student"));
                    v3.add(rs.getString("Student_Name"));
                }
                dft.addRow(v3);
            }

            pst.execute();
            pst.close();
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        jTable1.setBackground(Color.decode("#ff6600"));
        jTable1.setShowHorizontalLines(true);
        jTable1.setShowVerticalLines(true);

    }

